Create_Pollution_Predictions <- function(df, model, name = "Prediction",
                                         VarsToKeep = "Prediction"){
  
#########################
# Predictions
#########################


setwd(Main_Path)

# Predict NO2 Model
rxPredict(modelObject = model, 
          data = df,
          outData = paste0(Results_Path,"rxPollution Predictions.xdf"),
          predVarNames = name,
          type = "link",
          writeModelVars = TRUE,
          checkFactorLevels = F,
          extraVarsToWrite = VarsToKeep,
          overwrite = T)

}


